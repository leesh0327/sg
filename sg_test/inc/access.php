<?php

if( defined('_AUTH_') ) {

//-----------------------------------------------------------------------------------
switch( _AUTH_ ) {
//-----------------------------------------------------------------------------------
case 'ADMIN' :
//-----------------------------------------------------------------------------------

	if ($_SESSION['_admin']  <> "ROOT" ) {
			echo "<script>alert('섹션이 종료되었습니다 다시 로긴해 주세요 '); location.replace('index.php'); </script>\n";
			exit;		
	}

//-----------------------------------------------------------------------------------
break;
//-----------------------------------------------------------------------------------
case 'MEMBER' :
//-----------------------------------------------------------------------------------

	if ($_SESSION['_sign'] <> "USER" ) {
			echo "<script>alert('섹션이 종료되었습니다 다시 로긴해 주세요 '); </script>\n";
			echo "<script>location.replace('index.php'); </script>\n";
			exit;
	}

//-----------------------------------------------------------------------------------
break;
//-----------------------------------------------------------------------------------


} // end switch;

}
?>